from tkinter import *
import requests

root = Tk()

key = "KEY"
url = "http://api.ipstack.com/"

root.title("IP Locator")
root.geometry("500x500")


def getInfo():
    fullURL = (url + entry.get() + "?access_key=" + key)
    results = requests.get(fullURL)
    data = results.json()
    print(data)

    ip = data['ip']
    city = data['city']
    state = data['region_name']
    country = data['country_name']

    finalString = "Results for %s \nCity: %s \nState: %s \nCountry: %s" % (ip, city, state, country)
    outputLabel.configure(text=finalString)


title = Label(root, text="IP Locator", font="Helvetica, 20")
title.pack()

frame = Frame(root)
frame.pack(pady=20)

entry = Entry(frame)
entry.grid(row=0, column=0)

btn = Button(frame, text="Get Info", command=getInfo)
btn.grid(row=0, column=1)

outputLabel = Label(root, text="", font="Helvetica, 20")
outputLabel.pack(pady=20)

root.mainloop()
